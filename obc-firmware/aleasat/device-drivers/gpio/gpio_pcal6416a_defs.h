/**
 * @file gpio_pcal6416a_defs.h
 * @brief Register addresses for the PCAL6416A GPIO expander.
 */

#ifndef GPIO_PCAL6416A_DEFS_H_
#define GPIO_PCAL6416A_DEFS_H_

// Register Addresses
#define PCAL6416A_INPUT_PORT_0       0x00
#define PCAL6416A_INPUT_PORT_1       0x01
#define PCAL6416A_OUTPUT_PORT_0      0x02
#define PCAL6416A_OUTPUT_PORT_1      0x03
#define PCAL6416A_POL_INV_PORT_0     0x04
#define PCAL6416A_POL_INV_PORT_1     0x05
#define PCAL6416A_CONFIG_PORT_0      0x06
#define PCAL6416A_CONFIG_PORT_1      0x07
#define PCAL6416A_OUTPUT_DRV_STR_0   0x40
#define PCAL6416A_OUTPUT_DRV_STR_A0  0x41
#define PCAL6416A_OUTPUT_DRV_STR_1   0x42
#define PCAL6416A_OUTPUT_DRV_STR_A1  0x43
#define PCAL6416A_INPUT_LATCH_REG_0  0x44
#define PCAL6416A_INPUT_LATCH_REG_1  0x45
#define PCAL6416A_PULL_UP_DOWN_ENA_0 0x46
#define PCAL6416A_PULL_UP_DOWN_ENA_1 0x47
#define PCAL6416A_PULL_UP_DOWN_SEL_0 0x48
#define PCAL6416A_PULL_UP_DOWN_SEL_1 0x49
#define PCAL6416A_IRQ_MASK_0         0x4A
#define PCAL6416A_IRQ_MASK_1         0x4B
#define PCAL6416A_IRQ_STATUS_0       0x4C
#define PCAL6416A_IRQ_STATUS_1       0x4D
#define PCAL6416A_OUTPUT_PORT_CONFIG 0x4F

#endif // GPIO_PCAL6416A_DEFS_H_
